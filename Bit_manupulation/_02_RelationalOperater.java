public class _02_RelationalOperater {
    public static void main(String[] args) {


        // Relation value shows boolean values true and false 

        int A=10,B=5;
        // 1. Equal to Operater(==)
        System.out.println(A==B); //This statement print a boolean value false

        // 2. Not Equal to Operater(!=)
        System.out.println(A!=B); //This statement print a boolean value true because A is Not equal to B

        // 3. Greater than Operater(>)
        System.out.println(A>B); //This statement print a boolean value True Because A is Greater than B

        // 4. Less than Operater(<)
        System.out.println(A<B); //This statement print a boolean value false Because A is Not less than B

        // 5. Greater than equal to Operater(>=)
        System.out.println(A>=B); //This statement print a boolean value True Because A is not equal to B but gteater thab B

        // 6. Less than equal to Operater(<=)
        System.out.println(A<=B); //This statement print a boolean value false Because A is not less than B and Also not equal to B

        int c=10,d=10;
         // 5.1. Greater than equal to Operater(>=)
         System.out.println(c>=d); //This statement print a boolean value True Because A is equal to B

         // 6.1. Less than equal to Operater(<=)
         System.out.println(c<=d); //This statement print a boolean value true Because A is equal to  B
 

    }
    
}
